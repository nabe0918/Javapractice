package FinalExam;

public class program1 {

	public static void main(String[] args) {
		
	//Assuming that natural numbers start from 1
		
	    int n = 10, count = 1, sum = 0;

	       while(count <= n)
	       {
	           sum = sum + count;
	           count++;
	       }

	       System.out.println("Sum of first 10 natural numbers is: "+sum);

	}

}
